    public Cursor getCursor(String cla,String size,String weight,String from,String to,String not){
        SQLiteDatabase db=this.getReadableDatabase();
        String[] columns = {"id","class","sizelow","sizetop","weightlow","weighttop","from1","to1","price","time"};

        Cursor cursor = db.query("send", columns,"class ="+cla+" and sizelow <"+size+not+" and sizetop > "+size+
                " and weightlow<"+weight+" and weighttop>"+weight+" and from1=\""+from+"\" and to1=\""+to+"\"",null,null,null,null);

        return cursor;
    }


